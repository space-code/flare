import Foundation

/// Enum representing the labels for subscription store buttons.
public enum SubscriptionStoreButtonLabel {
    /// Multiline label for the button.
    case multiline

    /// Label displaying the price for the subscription.
    case price

    /// Label indicating the action of the button (e.g., "Subscribe").
    case action

    /// Label displaying the display name of the subscription.
    case displayName
}
